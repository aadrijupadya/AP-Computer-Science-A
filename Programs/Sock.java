public class Sock {
    private String color;
    private String type;

    public Sock() {
        color = "white";
        type = "ankle";
    }

    public Sock(String c, String t) {
        color = c;
        type = t;
    }

    public String getColor() {
        return color;
    }

    public String getType() {
        return type;
    }

}
